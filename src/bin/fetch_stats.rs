//! Fetch live Docker container counts from all homelab nodes via SSH.
//!
//! Run manually whenever you want to refresh the count baked into the site:
//!
//!     cargo run --bin fetch_stats > src/container_count.rs
//!
//! Envs:
//!   FAZ_LAB_PATH  Path to the faz-lab checkout (default: ../faz-lab)
//!
//! Reads the `nodes/` directory of faz-lab, SSHes into each subdir name,
//! runs `docker ps -a | wc -l`, and emits a Rust constant with the total.
//! Each line of `docker ps -a | wc -l` includes the header row, so we subtract
//! one per node.

use std::env;
use std::fs;
use std::path::PathBuf;
use std::process::Command;

fn main() {
    let faz_lab_path = env::var("FAZ_LAB_PATH")
        .map(PathBuf::from)
        .unwrap_or_else(|_| {
            let mut p = env::current_exe()
                .expect("cannot resolve current_exe")
                .parent()
                .expect("no parent")
                .to_path_buf();
            for _ in 0..5 {
                p.push("..");
            }
            p.join("faz-lab")
        });

    let nodes_dir = faz_lab_path.join("nodes");
    if !nodes_dir.is_dir() {
        eprintln!(
            "error: nodes directory not found at {} (set FAZ_LAB_PATH to your faz-lab checkout)",
            nodes_dir.display()
        );
        std::process::exit(1);
    }

    let mut nodes: Vec<String> = fs::read_dir(&nodes_dir)
        .expect("cannot read nodes dir")
        .filter_map(Result::ok)
        .filter(|e| e.file_type().map(|t| t.is_dir()).unwrap_or(false))
        .filter_map(|e| e.file_name().to_str().map(str::to_string))
        .collect();
    nodes.sort();

    if nodes.is_empty() {
        eprintln!(
            "error: no node subdirectories found in {}",
            nodes_dir.display()
        );
        std::process::exit(1);
    }

    eprintln!("Querying {} nodes via SSH:", nodes.len());
    let mut total: u32 = 0;
    for node in &nodes {
        let out = Command::new("ssh")
            .arg(node)
            .arg("docker ps -a | wc -l")
            .output();
        match out {
            Ok(o) if o.status.success() => {
                let stdout = String::from_utf8_lossy(&o.stdout).trim().to_string();
                let n: u32 = stdout
                    .split_whitespace()
                    .next()
                    .and_then(|s| s.parse().ok())
                    .unwrap_or(0);
                let real = n.saturating_sub(1);
                eprintln!("  {node:<30} {n} lines -> {real} containers");
                total = total.saturating_add(real);
            }
            Ok(o) => {
                let stderr = String::from_utf8_lossy(&o.stderr).trim().to_string();
                eprintln!("  {node:<30} FAILED: {stderr}");
            }
            Err(e) => {
                eprintln!("  {node:<30} could not spawn ssh: {e}");
            }
        }
    }

    eprintln!();
    eprintln!("Total active containers across all nodes: {total}");
    eprintln!();

    println!("//! Auto-generated by `cargo run --bin fetch_stats`.");
    println!("//! Source: live SSH query of ~/Projects/faz-lab/nodes/");
    println!("//! Do not edit by hand \u{2014} regenerate via the fetch_stats binary.");
    println!();
    println!("pub const CONTAINER_COUNT: u32 = {total};");
}
