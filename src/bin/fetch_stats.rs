use std::env;
use std::fs;
use std::path::Path;
use std::path::PathBuf;
use std::process::Command;

fn main() {
    let faz_lab_path = env::var("FAZ_LAB_PATH")
        .map(PathBuf::from)
        .unwrap_or_else(|_| PathBuf::from("/home/fazuh/Projects/faz-lab"));

    let server_count = count_servers(&faz_lab_path);
    let stack_count = count_compose_stacks(&faz_lab_path);
    let container_count = count_containers_via_ssh(&faz_lab_path);

    let src_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("src");
    let out_path = src_dir.join("server_stats.rs");

    let content = format!(
        r#"//! Auto-generated by `cargo run --bin fetch_stats`.
//! Source: live SSH query of ~/Projects/faz-lab/nodes/ + local compose count
//! Do not edit by hand — regenerate via the fetch_stats binary.

pub const SERVER_COUNT: u32 = {server_count};
pub const CONTAINER_COUNT: u32 = {container_count};
pub const STACK_COUNT: u32 = {stack_count};
"#,
    );

    fs::write(&out_path, &content).expect("failed to write container_count.rs");
    eprintln!(
        "Wrote {stack_count} stacks, {container_count} containers to {}",
        out_path.display()
    );
}

fn count_servers(faz_lab_path: &Path) -> u32 {
    let output = Command::new("ls")
        .args(["-1", faz_lab_path.join("nodes").to_str().unwrap()])
        .output()
        .expect("failed to get server count");

    if output.status.success() {
        let stdout = String::from_utf8_lossy(&output.stdout);
        let count = stdout.lines().filter(|l| !l.is_empty()).count() as u32;
        eprintln!("Servers found: {count}");
        count
    } else {
        let stderr = String::from_utf8_lossy(&output.stderr);
        eprintln!("ls failed: {stderr}");
        0
    }
}

fn count_compose_stacks(faz_lab_path: &Path) -> u32 {
    let output = Command::new("find")
        .arg(faz_lab_path.to_str().unwrap())
        .args(["-type", "d", "-name", "config", "-prune"])
        .args(["-o", "(", "-type", "f", "-o", "-type", "l", ")"])
        .args([
            "(",
            "-name",
            "docker-compose.yml",
            "-o",
            "-name",
            "docker-compose.yaml",
            ")",
        ])
        .arg("-print")
        .output()
        .expect("failed to get compose count");

    if output.status.success() {
        let stdout = String::from_utf8_lossy(&output.stdout);
        let count = stdout.lines().filter(|l| !l.is_empty()).count() as u32;
        eprintln!("Compose stacks found: {count}");
        count
    } else {
        let stderr = String::from_utf8_lossy(&output.stderr);
        eprintln!("find failed: {stderr}");
        0
    }
}

fn count_containers_via_ssh(faz_lab_path: &Path) -> u32 {
    let nodes_dir = faz_lab_path.join("nodes");
    if !nodes_dir.is_dir() {
        eprintln!(
            "error: nodes directory not found at {}",
            nodes_dir.display()
        );
        return 0;
    }

    let mut nodes: Vec<String> = fs::read_dir(&nodes_dir)
        .expect("cannot read nodes dir")
        .filter_map(Result::ok)
        .filter(|e| e.file_type().map(|t| t.is_dir()).unwrap_or(false))
        .filter_map(|e| e.file_name().to_str().map(str::to_string))
        .collect();
    nodes.sort();

    if nodes.is_empty() {
        eprintln!(
            "error: no node subdirectories found in {}",
            nodes_dir.display()
        );
        return 0;
    }

    eprintln!("Querying {} nodes via SSH:", nodes.len());
    let mut total: u32 = 0;
    for node in &nodes {
        let out = Command::new("ssh")
            .arg(node)
            .arg("docker ps -a | wc -l")
            .output();
        match out {
            Ok(o) if o.status.success() => {
                let stdout = String::from_utf8_lossy(&o.stdout).trim().to_string();
                let n: u32 = stdout
                    .split_whitespace()
                    .next()
                    .and_then(|s| s.parse().ok())
                    .unwrap_or(0);
                let real = n.saturating_sub(1);
                eprintln!("  {node:<30} {n} lines -> {real} containers");
                total = total.saturating_add(real);
            }
            Ok(o) => {
                let stderr = String::from_utf8_lossy(&o.stderr).trim().to_string();
                eprintln!("  {node:<30} FAILED: {stderr}");
            }
            Err(e) => {
                eprintln!("  {node:<30} could not spawn ssh: {e}");
            }
        }
    }

    eprintln!();
    eprintln!("Total active containers across all nodes: {total}");
    eprintln!();
    total
}
